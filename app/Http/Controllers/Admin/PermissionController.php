     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $input = $request->all();
        
        $per = new Permission;
        $per->name = $input['name'];
        $per->slug = $input['slug'];
        $per->save();

        return redirect(route('permission.index'));
    }


    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
       // dd($request->all());
        $inquiry = new  Inquiry();
        $inquiry->parent_name   = $request->parent_name;
        $inquiry->child_name    = $request->child_name;
        $inquiry->child_dob_age = $request->dob;
        $inquiry->phone         = $request->mobile;
        $inquiry->email         = $request->email;
        $inquiry->remark       = $request->message;
        $inquiry->save();

        Session::flash('inqury_success', 'Inquiry submited successfully');
        return back();
    }

